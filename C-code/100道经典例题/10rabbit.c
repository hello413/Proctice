#include<stdio.h>
/*有一对兔子，从出生后第3个月起每个月都生一对兔子，小兔子长到第三个月后每个月又生一对兔子，假如兔子都不死，问每个月的兔子总数为多少？*/
int main()
{
	int temp;
	int m1 = 1, m2 = 1;
	for (int i = 1; i <= 12; i++) {
		if (i == 1 || i == 2) {
			printf("第%d月的的兔子数量是：1对。\n", i);
		}
		else {
			//把相对于相对于当前月的上月赋值
			temp = m2;
			//求当前月的数量，同时也是下次的上月数量
			m2 = m1 + m2;
			//重新设置上上月兔子的数量
			m1 = temp;
			printf("第%d月的的兔子数量是：%d对。\n", i, m2);
		}
	}
	return 0;
}